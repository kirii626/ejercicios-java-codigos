package nea; // Definición del paquete 'nea'

/**
 * Clase punto1tp1deverdad para realizar operaciones aritméticas básicas entre dos números.
 */
public class punto1tp1deverdad {
    
    public static void main(String[] args) {
        int numuno = 7; // Declaración e inicialización de la variable numuno con valor 7
        int numdos = 5; // Declaración e inicialización de la variable numdos con valor 5
        
        // Mostrar el resultado de la suma de numuno y numdos
        System.out.println("El resultado de la suma es " + (numuno + numdos));
        
        // Mostrar el resultado de la resta de numuno y numdos
        System.out.println("El resultado de la resta es " + (numuno - numdos));
        
        // Mostrar el resultado de la multiplicación de numuno y numdos
        System.out.println("El resultado de la multiplicación es " + (numuno * numdos));
        
        // Mostrar el resultado de la división de numuno y numdos
        System.out.println("El resultado de la división es " + (numuno / numdos));
    }
}
